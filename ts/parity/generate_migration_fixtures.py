def _sql_literal(value: object) -> str:
    if value is None:
        return "NULL"
    if isinstance(value, (int, float)):
        return repr(value)
    if isinstance(value, bytes):
        return "X'" + value.hex() + "'"
    escaped = str(value).replace("'", "''")
    return f"'{escaped}'"


def _dump_sql(conn: sqlite3.Connection) -> str:
    """Dump the connection as portable SQL: all CREATE statements first (in
    sqlite_master's dependency-safe creation order), then all table data as
    INSERT statements.

    Deliberately NOT `conn.iterdump()`: its per-table interleaved schema+data
    grouping can order a table's INSERT before a virtual table its triggers
    reference (e.g. `memories`' data before `memories_fts`'s CREATE, if
    grouped alphabetically), which fires the AFTER-INSERT trigger against a
    not-yet-existing table on replay. Two-phase (all CREATEs, then all
    INSERTs) avoids this entirely: every trigger's target already exists by
    the time any data is replayed.

    FTS5-internal shadow tables are excluded from both phases — they are
    recreated automatically as a side effect of their owning virtual table's
    own CREATE VIRTUAL TABLE statement (see schema_inventory.py, which excludes
    them from structural comparison for the same reason).
    """
    objects = conn.execute(
        "SELECT type, name, sql FROM sqlite_master WHERE sql IS NOT NULL ORDER BY rowid"
    ).fetchall()
    virtual_tables = _virtual_table_names(conn)

    lines: list[str] = ["PRAGMA foreign_keys=OFF;"]
    for obj_type, name, sql in objects:
        if obj_type == "table" and _is_fts_shadow_table(name, virtual_tables):
            continue
        lines.append(sql.rstrip().rstrip(";") + ";")

    tables = [
        name
        for obj_type, name, _sql in objects
        if obj_type == "table" and not _is_fts_shadow_table(name, virtual_tables)
    ]
    for table in tables:
        columns = [row[1] for row in conn.execute(f'PRAGMA table_info("{table}")').fetchall()]
        if not columns:
            continue
        column_list = ", ".join(f'"{c}"' for c in columns)
        for row in conn.execute(f'SELECT {column_list} FROM "{table}"').fetchall():
            values = ", ".join(_sql_literal(value) for value in row)
            lines.append(f'INSERT INTO "{table}" ({column_list}) VALUES ({values});')
    lines.append("PRAGMA foreign_keys=ON;")
    return "\n".join(lines)


def _row_dict(row: sqlite3.Row | None) -> dict | None:
    return dict(row) if row is not None else None


def _capture_expected(seed_sql: str) -> dict:
    """Run the REAL Python engine forward to completion from this seed;
    capture the schema inventory, the full applied-id set, and row-level facts
    relevant to Track B's acceptance criteria."""
    conn = sqlite3.connect(":memory:")
    conn.row_factory = sqlite3.Row
    conn.execute("PRAGMA foreign_keys=ON")
    conn.executescript(seed_sql)
    run_migrations(conn)

    inventory = build_schema_inventory(conn)
    applied = sorted(row["id"] for row in conn.execute("SELECT id FROM _migrations"))
    identity_layers = sorted(
        row["layer"] for row in conn.execute("SELECT layer FROM identity ORDER BY layer")
    )
    conversation_journeys = [
        row["journey"] for row in conn.execute("SELECT journey FROM conversations ORDER BY id")
    ]
    memory_journeys = [
        row["journey"] for row in conn.execute("SELECT journey FROM memories ORDER BY id")
    ]
    memory_legacy_row = _row_dict(
        conn.execute(
            "SELECT title, content, use_count, readiness_state, journey "
            "FROM memories WHERE id = 'mem-legacy-1'"
        ).fetchone()
    )
    attachment_row = _row_dict(
        conn.execute(
            "SELECT journey_id, name, content FROM attachments WHERE id = 'att-legacy-1'"
        ).fetchone()
    )
    task_row = _row_dict(
        conn.execute(
            "SELECT journey, title, status FROM tasks WHERE id = 'task-legacy-1'"
        ).fetchone()
    )
    fts_hits = [
        row["rowid"]
        for row in conn.execute("SELECT rowid FROM memories_fts WHERE memories_fts MATCH 'legacy'")
    ]
    conn.close()
    return {
        "tables": inventory["tables"],
        "indexes": inventory["indexes"],
        "triggers": inventory["triggers"],
        "applied_migration_ids": applied,
        "identity_layers": identity_layers,
        "conversation_journeys": conversation_journeys,
        "memory_journeys": memory_journeys,
        "memory_legacy_row": memory_legacy_row,
        "attachment_legacy_row": attachment_row,
        "task_legacy_row": task_row,
        "memories_fts_findable_legacy_row_count": len(fts_hits),
    }


def _write_fixture(stem: str, seed_sql: str) -> None:
    expected = _capture_expected(seed_sql)
    (FIXTURES_DIR / f"migration-{stem}-pre-state.sql").write_text(seed_sql, encoding="utf-8")
    (FIXTURES_DIR / f"migration-{stem}-expected.json").write_text(
        json.dumps(expected, indent=2, sort_keys=True) + "\n", encoding="utf-8"
    )


def generate() -> None:
    FIXTURES_DIR.mkdir(parents=True, exist_ok=True)

    conn = sqlite3.connect(":memory:")
    conn.row_factory = sqlite3.Row
    conn.execute("PRAGMA foreign_keys=ON")
    conn.executescript(GENESIS_SQL)
    conn.commit()

    _write_fixture("chain-multi-hop", GENESIS_SQL)

    conn.execute(
        "CREATE TABLE IF NOT EXISTS _migrations (id TEXT PRIMARY KEY, applied_at TEXT NOT NULL)"
    )
    conn.commit()

    for migration_id, apply in MIGRATIONS:
        if migration_id in CHECKPOINTS:
            stem = migration_id.split("_", 1)[0]
            _write_fixture(stem, _dump_sql(conn))

        apply(conn)
        conn.execute(
            "INSERT OR IGNORE INTO _migrations (id, applied_at) VALUES (?, ?)",
            (migration_id, _now()),
        )
        conn.commit()

        if migration_id in POST_MIGRATION_INSERTS:
            conn.executescript(POST_MIGRATION_INSERTS[migration_id])
            conn.commit()

    conn.close()


if __name__ == "__main__":
    generate()
    print(f"Generated fixtures under {FIXTURES_DIR}")
