"""Generate committed migration-transition fixtures for CV22.DS6.TS2 (Track B).

Checkpoint N's "pre-state" fixture is the database exactly as it exists after
migrations 1..(N-1) have been applied for real via Python's own MIGRATIONS
functions (the frozen oracle) — so migration N's real transition logic is
genuinely exercised when the ported (TS) and oracle (Python) engines each run
forward from that seed, rather than testing an empty-table edge case that
never occurs on a real installed database. Representative rows are inserted
between certain steps so rename/backfill logic acts on real pre-existing data.

Checkpoints are generated by a SINGLE forward cascade from one hand-authored
genesis seed (the oldest addressable legacy shape, pre-migration-001) — not
nine independently hand-crafted seeds. This mirrors how a real database's
history actually looks (genesis -> 1 -> 2 -> ... -> 16), gives the multi-hop
fixture "for free" as the final step of the same cascade, and keeps the nine
checkpoints DRY.

Known gap (see CV22.DS6.TS2 done.md / debt review): migration 016's real
ADD-COLUMN-if-missing + backfill-against-NULL-rows branches cannot be reached
by this cascade, because the CURRENT migration 015 already creates
`display_code TEXT NOT NULL` inline — there are no pre-existing NULL rows by
the time the cascade reaches checkpoint 016. Exercising 016's real logic needs
a separately hand-authored fixture representing a database created by an
OLDER version of migration 015 (before display_code existed there), which is
out of this generator's scope.

Two fixture kinds are committed per checkpoint, under ts/test/fixtures/migrations/:
- `migration-<NNN>-pre-state.sql` (or `migration-chain-multi-hop-pre-state.sql`):
  a portable SQL script (CREATE TABLE + INSERT + partial `_migrations` ledger)
  executable identically by Python (`executescript`) and TS (`node:sqlite`'s
  `.exec()`).
- `migration-<NNN>-expected.json`: captured by running Python's REAL
  `run_migrations` forward from that seed to completion (migration 16) —
  schema inventory (reusing `schema_inventory.build_schema_inventory`), the
  full `_migrations` id set, and fixture-specific row-level facts.

Regenerate via: uv run python ts/parity/generate_migration_fixtures.py
"""

